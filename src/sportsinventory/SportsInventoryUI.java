    private void showInvButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showInvButtonActionPerformed

        int numOfBalls, numOfBats, numOfCaps, numOfSafety, numOfShoes, numOfClothes;

        numOfBalls = balls.size();
        numOfCaps = caps.size();
        numOfBats = Bats.size();
        numOfSafety = safetyWear.size();
        numOfShoes = shoes.size();
        numOfClothes = clothes.size();
        

        outputLabel.setText("Number of Balls: " + numOfBalls
                + "\nNumber of Bats: " + numOfBats + "\nNumber of Caps: " + numOfCaps + "\nNumber of Safety Wear Items: " + numOfSafety + "\nNumber of Shoes: "
                + numOfShoes + "\nNumber of Clothes: " + numOfClothes);
        
        for (int i=0; i<balls.size(); i++)
            outputLabel.setText(outputLabel.getText() + "\n" + i + ": " + balls.get(i));

    }//GEN-LAST:event_showInvButtonActionPerformed

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String type;
        int number;

        number = Integer.parseInt(numOfInvInput.getText());
        type = typeOfInvInput.getText();

        switch (type) {
            case "Balls":
            case "balls":
                outputLabel.setText(number + " Balls Added");
                ballsAdded();
                break;
            case "Bats":
            case "bats":
                outputLabel.setText(number + " Bats Added");
                batsAdded();
                break;
            case "Shoes":
            case "shoes":
                outputLabel.setText(number + " Shoes Added");
                shoesAdded();
                break;
            case "Clothes":
            case "clothes":
                outputLabel.setText(number + " Clothes Added");
                clothesAdded();
                break;
            case "caps":
            case "Caps":
                outputLabel.setText(number + " Caps Added");
                capsAdded();
                break;
            case "Safety":
            case "safety":
            case "safety wear":
            case "Safety Wear":
                outputLabel.setText(number + " Safety Wear Added");
                safetyAdded();
                break;
            default:
                outputLabel.setText("Error");
                break;
        }


    }//GEN-LAST:event_addButtonActionPerformed

