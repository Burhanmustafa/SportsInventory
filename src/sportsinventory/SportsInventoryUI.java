    private void ballsAdded() {
        String name;
        int number;
        number = Integer.parseInt(numOfInvInput.getText());
        name = nameInput.getText();

        for (int i = 0; i < number; i++) {
            balls.add(name);
        }
    }

    private void ballsRemoved() {
        int number;
        number = Integer.parseInt(numOfInvInput.getText());

        for (int c = 0; c < number; c++) {
            balls.remove(balls.size() - 1);
        }
    }

